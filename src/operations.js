/**
 * Performs an Addition
 * @param {*} a left value
 * @param {*} b right value
 */
module.exports.addition = (a, b) => a + b;

/**
 * Performs a Subtraction
 * @param {*} a left value
 * @param {*} b right value
 */
module.exports.subtraction = (a, b) => a - b;

/**
 * Performs a Division
 * @param {*} a left value
 * @param {*} b right value
 */
module.exports.division = (a, b) => a / b;

/**
 * Performs a Multiplication
 * @param {*} a left value
 * @param {*} b right value
 */
module.exports.multiplication = (a, b) => a * b;

/**
 * Performs a Remainder
 * @param {*} a left value
 * @param {*} b right value
 */
module.exports.remainder = (a, b) => a % b;
